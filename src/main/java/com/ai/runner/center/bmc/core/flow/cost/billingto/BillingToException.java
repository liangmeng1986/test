package com.ai.runner.center.bmc.core.flow.cost.billingto;

public class BillingToException extends Exception {

	public BillingToException() {
		super();
	}

	public BillingToException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public BillingToException(String message, Throwable cause) {
		super(message, cause);
	}

	public BillingToException(String message) {
		super(message);
	}

	public BillingToException(Throwable cause) {
		super(cause);
	}

}
